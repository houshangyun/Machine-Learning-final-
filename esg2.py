import json, torch, warnings
import pandas as pd
import torch.nn as nn
import torch.nn.functional as F
import numpy as np
from torch.utils.data import Dataset, DataLoader
from transformers import BertTokenizer, BertModel, get_linear_schedule_with_warmup
from sklearn.metrics import f1_score
from sklearn.model_selection import KFold

warnings.filterwarnings('ignore')

# ============================================================
# Step 1: 超參數與權重設定
# ============================================================
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"目前使用的硬體加速裝置: {device}")

MODEL_NAME = "google-bert/bert-base-chinese"
MAX_LEN = 512
BATCH_SIZE = 16
EPOCHS = 10
LR = 2e-5
N_SPLITS = 5
SEED = 42
RDROP_ALPHA = 1

EVAL_FIELDS = {
    "promise_status": ["Yes", "No"],
    "verification_timeline": ["already", "within_2_years", "between_2_and_5_years", "more_than_5_years", "N/A"],
    "evidence_status": ["Yes", "No", "N/A"],
    "evidence_quality": ["Clear", "Not Clear", "Misleading", "N/A"]
}
FIELD_WEIGHTS = {"promise_status": 0.2, "verification_timeline": 0.15, "evidence_status": 0.3, "evidence_quality": 0.35}
label2id = {field: {label: i for i, label in enumerate(labels)} for field, labels in EVAL_FIELDS.items()}
id2label = {field: {i: label for i, label in enumerate(labels)} for field, labels in EVAL_FIELDS.items()}

WEIGHTS = {
    'promise_status': None,
    'verification_timeline': None,
    'evidence_status': None,
    'evidence_quality': None
}

# ============================================================
# Step 2: 改用 JSON 安全載入（徹底避開 CSV 編碼與引號地雷）
# ============================================================
print("⏳ 正在安全載入 JSON 資料集...")

with open("vpesg_4k_train_1000.json", "r", encoding="utf-8") as f:
    train_list = json.load(f)

with open("vpesg4k_val_1000.json", "r", encoding="utf-8") as f:
    val_list = json.load(f)

with open("vpesg4k_test_2000.json", "r", encoding="utf-8") as f:
    test_list = json.load(f)

# 統一微調清洗標籤資料
for data_list in [train_list, val_list]:
    for item in data_list:
        if item.get('verification_timeline') == 'longer_than_5_years':
            item['verification_timeline'] = 'more_than_5_years'
        for f in EVAL_FIELDS:
            if item.get(f) is None or item[f] == "":
                item[f] = 'N/A'

# 合併 Train & Val 做 5-Fold 最大化泛化
all_train_data = train_list + val_list
all_test_data = test_list

print(f"✅ 載入成功！訓練大池總數: {len(all_train_data)} 筆, 測試集總數: {len(all_test_data)} 筆")

# ============================================================
# Step 3: 模型與對抗訓練類別定義
# ============================================================
class FGM():
    def __init__(self, model):
        self.model = model
        self.backup = {}
    def attack(self, epsilon=1.0, emb_name='word_embeddings'):
        for name, param in self.model.named_parameters():
            if param.requires_grad and emb_name in name:
                self.backup[name] = param.data.clone()
                norm = torch.norm(param.grad)
                if norm != 0 and not torch.isnan(norm):
                    param.data.add_(epsilon * param.grad / norm)
    def restore(self, emb_name='word_embeddings'):
        for name, param in self.model.named_parameters():
            if param.requires_grad and emb_name in name:
                param.data = self.backup[name]
        self.backup = {}

class MultiTaskBERT(nn.Module):
    def __init__(self, num_labels_dict):
        super().__init__()
        self.bert = BertModel.from_pretrained(MODEL_NAME, output_hidden_states=True)
        hidden_size = self.bert.config.hidden_size
        self.layer_weights = nn.Parameter(torch.ones(12))
        self.dropout = nn.Dropout(0.3)
        self.classifiers = nn.ModuleDict({f: nn.Linear(hidden_size, n) for f, n in num_labels_dict.items()})

    def forward(self, input_ids, attention_mask):
        outputs = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        all_hidden_states = outputs.hidden_states[1:]
        weights = F.softmax(self.layer_weights, dim=0)
        out = sum(w * h for w, h in zip(weights, all_hidden_states))
        pooled = out[:, 0, :]
        return {f: clf(self.dropout(pooled)) for f, clf in self.classifiers.items()}

